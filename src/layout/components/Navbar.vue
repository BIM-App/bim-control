          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">退出账号</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import { getUser, removeMember, removeProjectPID, removeUser, removeRole } from '@/utils/cookie'
import eventVue from '@/utils/eventVue'
export default {
  components: {
    Breadcrumb,
    Hamburger
  },
  data() {
    return {
      avatar: ''
    }
  },
  computed: {
    ...mapGetters([
      'sidebar'
      // 'avatar'
    ])
  },
  created() {
    // 接收从Profile组件传来的avatar值
    eventVue.$on('avatar', (avatar) => {
      // 一些操作，message就是从top组件传过来的值
      // console.log(message)
      this.avatar = avatar
    })
    // console.log(getUser().avatar)
    this.avatar = getUser().avatar
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },

    async logout() {
      await this.$store.commit('user/SET_USER', '')
      removeUser()
      removeMember()
      removeProjectPID()
      removeRole()
      sessionStorage.removeItem('uid')
      await this.$router.replace(`/login?redirect=${this.$route.fullPath}`)
      location.reload()
    }
  }
}
</script>

